class User
  attr_accessor :name, :email, :birthday
  
  @profiledescription = ""
  
  def initialize (attributes ={})
    @name = attributes[:name]
    @email = attributes[:email]
    @birthday = attributes [:birthday]
  end
  
  def printemail
    puts "#{@email}"
  end
  
  def parsebirthday
    @birthday.split('/')
  end
end
